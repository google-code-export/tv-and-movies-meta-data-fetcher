package org.stanwood.media.store.mp4;

public class AtomicParsleyException extends Exception {

	private static final long serialVersionUID = -4302544872869026705L;

	public AtomicParsleyException() {
		
	}

	public AtomicParsleyException(String arg0) {
		super(arg0);
		
	}

	public AtomicParsleyException(Throwable arg0) {
		super(arg0);
	
	}

	public AtomicParsleyException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	
	}

}
